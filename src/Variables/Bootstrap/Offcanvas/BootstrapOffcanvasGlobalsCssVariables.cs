namespace Soenneker.Quark;

/// <summary>
/// Bootstrap's offcanvas global CSS variables
/// </summary>
public sealed class BootstrapOffcanvasGlobalsCssVariables
{
	[CssVariable("bs-offcanvas-width")]
	public string? OffcanvasWidth { get; set; }

	[CssVariable("bs-offcanvas-height")]
	public string? OffcanvasHeight { get; set; }

	[CssVariable("bs-offcanvas-padding-x")]
	public string? OffcanvasPaddingX { get; set; }

	[CssVariable("bs-offcanvas-padding-y")]
	public string? OffcanvasPaddingY { get; set; }

	[CssVariable("bs-offcanvas-color")]
	public string? OffcanvasColor { get; set; }

	[CssVariable("bs-offcanvas-bg")]
	public string? OffcanvasBg { get; set; }

	[CssVariable("bs-offcanvas-border-width")]
	public string? OffcanvasBorderWidth { get; set; }

	[CssVariable("bs-offcanvas-border-color")]
	public string? OffcanvasBorderColor { get; set; }

	[CssVariable("bs-offcanvas-box-shadow")]
	public string? OffcanvasBoxShadow { get; set; }

	[CssVariable("bs-offcanvas-title-line-height")]
	public string? OffcanvasTitleLineHeight { get; set; }

	[CssVariable("bs-offcanvas-close-size")]
	public string? OffcanvasCloseSize { get; set; }

	[CssVariable("bs-offcanvas-zindex")]
	public string? OffcanvasZindex { get; set; }
}


