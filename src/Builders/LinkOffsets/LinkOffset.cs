namespace Soenneker.Quark;

/// <summary>
/// Static utility class for creating link offset builders with predefined values.
/// </summary>
public static class LinkOffset
{
    /// <summary>
    /// Gets a link offset builder with value 1.
    /// </summary>
    public static LinkOffsetBuilder Is1 => new(1);
    /// <summary>
    /// Gets a link offset builder with value 2.
    /// </summary>
    public static LinkOffsetBuilder Is2 => new(2);
    /// <summary>
    /// Gets a link offset builder with value 3.
    /// </summary>
    public static LinkOffsetBuilder Is3 => new(3);
}






