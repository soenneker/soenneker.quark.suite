namespace Soenneker.Quark;

/// <summary>
/// Represents the color picker format values.
/// </summary>
public enum ColorPickerFormat
{
    /// <summary>
    /// Represents the hex value.
    /// </summary>
    Hex,
    /// <summary>
    /// Represents the rgb value.
    /// </summary>
    Rgb,
    /// <summary>
    /// Represents the css value.
    /// </summary>
    Css,
    /// <summary>
    /// Represents the hsl value.
    /// </summary>
    Hsl
}
